# Split the input string into lines
lines = input_string.split('\n')

# Create a dictionary to store the latest true/false value for each agent
latest_values = {}

# Iterate through each line in reverse order (from the end of the string)
for line in reversed(lines):
    # Split each line by ':' to separate agent name from the value
    parts = line.split(':')

    # Check if the line contains "Agent" and if it has two parts
    if len(parts) == 2 and parts[0].strip().startswith("Agent"):
        agent_name = parts[0].strip()
        value = parts[1].strip()

        # Update the latest value for the agent if not already present
        if agent_name not in latest_values:
            latest_values[agent_name] = value.lower()  # Store the lowercase value

# Count the number of agents ending with true and false values
true_count = sum(1 for value in latest_values.values() if value == "true")
false_count = sum(1 for value in latest_values.values() if value == "false")

# Print the latest true/false values for each agent
print("Latest true/false values for each agent:")
for agent, value in latest_values.items():
    if value == "true":
        print(f"{agent}: {value}")

# Print the counts of agents ending with 'true' and 'false'
print("Number of agents ending with 'true':", true_count)
print("Number of agents ending with 'false':", false_count)


